import java.util.ArrayList;
public class Arraylist {
    public static void main(String[] args) {
        ArrayList<String> color = new ArrayList<String>();
         color.add("Blue");
         color.add("Black");
         color.add("White");
         color.add("Yellow");
         for(String i:color)
         {
            System.out.println(i);
         }


    }
}
